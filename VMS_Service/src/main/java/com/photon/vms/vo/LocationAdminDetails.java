package com.photon.vms.vo;

public class LocationAdminDetails {

	private String locAdminCode;
	private String locAdminName;
	private String locAdminEmail;
	private String companyName;
	private int isAdmin;
	
	public String getLocAdminCode() {
		return locAdminCode;
	}
	public void setLocAdminCode(String locAdminCode) {
		this.locAdminCode = locAdminCode;
	}
	public String getLocAdminName() {
		return locAdminName;
	}
	public void setLocAdminName(String locAdminName) {
		this.locAdminName = locAdminName;
	}
	public String getLocAdminEmail() {
		return locAdminEmail;
	}
	public void setLocAdminEmail(String locAdminEmail) {
		this.locAdminEmail = locAdminEmail;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public int getIsAdmin() {
		return isAdmin;
	}
	public void setIsAdmin(int isAdmin) {
		this.isAdmin = isAdmin;
	}
	
	
	
	
	
}
