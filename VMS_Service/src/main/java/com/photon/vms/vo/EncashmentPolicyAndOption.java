package com.photon.vms.vo;

public class EncashmentPolicyAndOption {

	private int leaveEncashId;
	private String flag;
	private String mastDescription;
	private int mastValue;
	private int isSelected;
	public int getLeaveEncashId() {
		return leaveEncashId;
	}
	public void setLeaveEncashId(int leaveEncashId) {
		this.leaveEncashId = leaveEncashId;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getMastDescription() {
		return mastDescription;
	}
	public void setMastDescription(String mastDescription) {
		this.mastDescription = mastDescription;
	}
	public int getMastValue() {
		return mastValue;
	}
	public void setMastValue(int mastValue) {
		this.mastValue = mastValue;
	}
	public int getIsSelected() {
		return isSelected;
	}
	public void setIsSelected(int isSelected) {
		this.isSelected = isSelected;
	}
	
	
}
